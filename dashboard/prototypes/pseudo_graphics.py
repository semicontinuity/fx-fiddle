LEGACY_SYMBOL_START = 0x1FB00
# Correct bitmasks for the four missing glyphs in the Unicode block.
# 0b000000 (0), 0b010101 (21), 0b101010 (42), 0b111111 (63)
MISSING_GLYPH_BITMASKS = {0, 21, 42, 63}


def build_bitmask_map():
    _map = [''] * 64
    # Manually map the missing glyphs to their replacements.
    _map[0b000000] = ' '
    _map[0b010101] = '\u258C'  # Left half block
    _map[0b101010] = '\u2590'  # Right half block
    _map[0b111111] = '\u2588'  # Full block

    char_code = LEGACY_SYMBOL_START
    for i in range(64):
        # If the bitmask corresponds to a missing glyph, skip it.
        if i in MISSING_GLYPH_BITMASKS:
            continue
        _map[i] = chr(char_code)
        char_code += 1
    return _map


BITMASK_MAP = build_bitmask_map()


