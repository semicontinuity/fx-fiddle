def remap_bitmask_to_char(bitmask: int) -> str:
    return BITMASK_MAP[bitmask]


def render_text(text: str) -> None:
    output_grid_height = 6  # 16 rows / 3 pixels per glyph row (rounded up)
    output_grid_width = 4   # 8 pixels / 2 pixels per glyph

    # Initialize the grid that will hold the final pseudographic characters
    glyph_grid = [['' for _ in range(len(text) * output_grid_width)] for _ in range(output_grid_height)]

    for char_idx, char_code in enumerate(text):
        bitmap = ALL_CHARS_BITMAP.get(char_code)

        # Render each 8x16 character as a 4x6 grid of 2x3 pseudographic glyphs
        for y_glyph in range(output_grid_height):
            for x_glyph in range(output_grid_width):
                bitmask = 0
                # Each glyph is made of a 2x3 block of pixels from the original bitmap
                for y_pixel_in_glyph in range(3):
                    for x_pixel_in_glyph in range(2):
                        # Calculate the row and column in the 8x16 bitmap
                        bitmap_row = y_glyph * 3 + y_pixel_in_glyph
                        bitmap_col = x_glyph * 2 + x_pixel_in_glyph

                        # If the character is not in our font or we are out of bounds for the bitmap,
                        # treat the pixel as off.
                        pixel_is_on = 0
                        if bitmap and bitmap_row < 16:
                            # Get the byte for the current row
                            byte = bitmap[bitmap_row]
                            # Check if the specific bit for our pixel is set
                            pixel_is_on = (byte >> (7 - bitmap_col)) & 1

                        if pixel_is_on:
                            # The bit index for the 2x3 glyph is calculated from top to bottom,
                            # then left to right.
                            bit_index = (y_pixel_in_glyph * 2) + x_pixel_in_glyph
                            bitmask |= (1 << bit_index)

                # Map the final 6-bit mask to a Unicode character and place it in our output grid
                grid_col = char_idx * output_grid_width + x_glyph
                glyph_grid[y_glyph][grid_col] = remap_bitmask_to_char(bitmask)

    # Print the final rendered text
    for row in glyph_grid:
        print("".join(row))


def main():
    if len(sys.argv) > 1:
        text_to_display = sys.argv[1]
        render_text(text_to_display)
    else:
        # Display all printable ASCII characters as a demonstration
        print("--- FULL FONT DEMO ---")
        all_printable_chars = "".join(chr(i) for i in range(ord(' '), ord('~') + 1))
        render_text(all_printable_chars)


